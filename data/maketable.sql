CREATE TABLE fooddata (
	id INT AUTO_INCREMENT PRIMARY KEY, 
	`group` VARCHAR(12) NOT NULL, 
	type1 VARCHAR(10) NOT NULL,
	type2 VARCHAR(10) NOT NULL, 
	type3 DECIMAL(38, 0) NOT NULL, 
	fdname VARCHAR(100) NOT NULL, 
	size DECIMAL(38, 2) NOT NULL, 
	kcal DECIMAL(38, 2) NOT NULL, 
	carbon DECIMAL(38, 2) NOT NULL, 
	protein DECIMAL(38, 2) NOT NULL, 
	fat DECIMAL(38, 2) NOT NULL, 
	sugar DECIMAL(38, 2) NOT NULL, 
	na DECIMAL(38, 2) NOT NULL, 
	chol DECIMAL(38, 2) NOT NULL, 
	saturatedfat DECIMAL(38, 2) NOT NULL, 
	transfat DECIMAL(38, 2) NOT NULL, 
	year DECIMAL(4, 0) NOT NULL, 
	hbp BOOL NOT NULL, 
	diabetes BOOL NOT NULL, 
	diet BOOL NOT NULL, 
	salty int(10) unsigned NOT NULL, 
	sweet int(10) unsigned NOT NULL, 
	sour int(10) unsigned NOT NULL, 
	hot int(10) unsigned NOT NULL, 
	CHECK (hbp IN (0, 1)), 
	CHECK (diabetes IN (0, 1)), 
	CHECK (diet IN (0, 1))
);
